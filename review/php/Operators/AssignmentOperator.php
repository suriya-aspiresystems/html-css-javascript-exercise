<?php
// Simple assign operator
$y = 75;
echo $y, "<br>";

// Add then assign operator
$y = 100;
$y += 200;
echo $y, "<br>";

// Subtract then assign operator
$y = 70;
$y -= 10;
echo $y, "<br>";

// Multiply then assign operator
$y = 30;
$y *= 20;
echo $y, "<br>";

// Divide then assign(quotient) operator
$y = 100;
$y /= 5;
echo $y, "<br>";

// Divide then assign(remainder) operator
$y = 50;
$y %= 5;
echo $y;
?>